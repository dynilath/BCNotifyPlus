let ImageStorage: Map<keyof typeof Icons, HTMLImageElement> = new Map();

export function GetIcon(tag: keyof typeof Icons) {
    let img = ImageStorage.get(tag);
    if (!img) {
        img = new Image();
        img.src = Icons[tag];
        ImageStorage.set(tag, img);
    }
    return img;
}